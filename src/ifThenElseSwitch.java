public class ifThenElseSwitch {
    public static void main(String arg[]) {
        int i = 2;
        int x = 2;

        if(i < x) {
            System.out.println("TRUE");
        } else if (i == x) {
            System.out.println("EQUAL");
        } else {
            System.out.println("FALSE");
        }
        
    }
}